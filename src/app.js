//////////////////////////////////////////////////////
// INCLUDES
//////////////////////////////////////////////////////
const express=require("express")

//////////////////////////////////////////////////////
// CREATE APP
//////////////////////////////////////////////////////
const app=express()

//////////////////////////////////////////////////////
// USES
//////////////////////////////////////////////////////
app.use(express.json());
app.use(express.urlencoded({extended:false}));

app.use("/", express.static('public'));
//////////////////////////////////////////////////////
// EXPORT APP
//////////////////////////////////////////////////////
const PORT=3000;

//////////////////////////////////////////////////////
// START SERVER
//////////////////////////////////////////////////////
app.listen(PORT,()=>{
    console.log(`The server is running on port ${PORT}`)
})


